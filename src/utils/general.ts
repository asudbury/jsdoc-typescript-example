/**
    Calculates the sum of two numbers.
    @function
    @category General
    @param {number} a - The first number to add.
    @param {number} b - The second number to add.
    @returns {number} The sum of `a` and `b`.
*/
export const addTwoNumbers = (a: number, b: number): number => {
    return a + b;
}
